def fix_nouns(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        raw = f.read()
        data = json.loads(raw)

    # Determine structure
    if isinstance(data, list):
        items = data
    elif 'nouns' in data:
        items = data['nouns']
    elif 'items' in data:
        items = data['items']
    elif 'data' in data:
        items = data['data']
    else:
        # might be dict of categories
        items = []
        for v in data.values():
            if isinstance(v, list):
                items.extend(v)

    fixed_count = 0
    for item in items:
        word = item.get('word', '')
        old_meaning = item.get('meaning', '')
        if word in CORRECTIONS:
            new_meaning = CORRECTIONS[word]
            if old_meaning != new_meaning:
                print(f"  {word}: \"{old_meaning}\" → \"{new_meaning}\"")
                item['meaning'] = new_meaning
                fixed_count += 1

    print(f"\nFixed {fixed_count} entries")

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')

    return fixed_count

if __name__ == '__main__':
    fix_nouns('public/data/nouns.json')
